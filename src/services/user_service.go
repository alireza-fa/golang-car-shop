func (s *UserService) getDefaultRole() (roleId int, err error) {

	if err = s.database.Model(&models.User{}).
		Select("id").
		Where("name = ?", constants.DefaultRoleName).
		First(&roleId).
		Error; err != nil {
		return 0, err
	}
	return roleId, nil
}
